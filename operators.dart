//!   +  -  *  /  ~/  %

void main() {
  int a = 10;
  int b = 2;
  int c = 3; 

  print('a + b = ${a + b}');  // 12
  print('a - b = ${a - b}'); // 8
  print('a * b = ${a * b}'); // 20
  print('a / b = ${a / c}'); // 3.333...
  print('a ~/ b = ${a ~/ b}'); // 5 (int division)
  print('a % b = ${a % c}'); //  1 (remainder)
}


